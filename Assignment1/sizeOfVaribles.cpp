#include<iostream>

using namespace std;

int main() {

    // list data types
    // int 
    int a;
    float b;
    double c;
    char d;
    short e;
    long f;
    long long g;
    long double h;
    bool i;

    cout<<"size of  int:  "<<sizeof(a)<<endl;
    cout<<"size of  float:  "<<sizeof(b)<<endl;
    cout<<"size of  double:  "<<sizeof(c)<<endl;
    cout<<"size of  char:  "<<sizeof(d)<<endl;
    cout<<"size of  short:  "<<sizeof(e)<<endl;
    cout<<"size of  long:  "<<sizeof(f)<<endl;
    cout<<"size of  long long:  "<<sizeof(g)<<endl;
    cout<<"size of  long double:  "<<sizeof(h)<<endl;
    cout<<"size of  bool:  "<<sizeof(i)<<endl;


    return 0;
}